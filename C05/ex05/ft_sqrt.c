int	ft_sqrt(int nb)
{
	long	sqrt;

	sqrt = 0;
	while (nb >= sqrt * sqrt)
	{
		if (nb == sqrt * sqrt)
			return ((int) sqrt);
		sqrt ++;
	}
	return (0);
}
